#include <iostream>
#include <string>

int main() {
    // 원본 문자열
    std::string number_string =
        "73167176531330624919225119674426574742355349194934"
        "96983520312774506326239578318016984801869478851843"
        "85861560789112949495459501737958331952853208805511"
        "12540698747158523863050715693290963295227443043557"
        "66896648950445244523161731856403098711121722383113"
        "62229893423380308135336276614282806444486645238749"
        "30358907296290491560440772390713810515859307960866"
        "70172427121883998797908792274921901699720888093776"
        "65727333001053367881220235421809751254540594752243"
        "52584907711670556013604839586446706324415722155397"
        "53697817977846174064955149290862569321978468622482"
        "83972241375657056057490261407972968652414535100474"
        "82166370484403199890008895243450658541227588666881"
        "16427171479924442928230863465674813919123162824586"
        "17866458359124566529476545682848912883142607690042"
        "24219022671055626321111109370544217506941658960408"
        "07198403850962455444362981230987879927244284909188"
        "84580156166097919133875499200524063689912560717606"
        "05886116467109405077541002256983155200055935729725"
        "71636269561882670428252483600823257530420752963450";

    int len = 13; // 특정 길이
    long long max_result = 0; // 최대값 저장 변수
    int i = 0; // 문자열 시작 위치

    while (i <= number_string.size() - len) {
        // 문자열 자르기
        std::string window = number_string.substr(i, len);

        // 자른 문자열에 '0'이 있으면 건너뛰기
        int zero_pos = window.find('0');
        if (zero_pos != std::string::npos) {
            i += zero_pos + 1;
            continue;
        }

        // 해당 문자열 곱 계산
        long long current_result = 1;
        for (char digit : window) {
            current_result *= (digit - '0');
        }

        // 서로 비교
        if (current_result > max_result) {
            max_result = current_result;
        }

        i++;
    }

    std::cout << max_result << std::endl;
    return 0;
}
